#  integer data type
age = 30
print(f"Variable 'age': {age}")

# float data type
price = 19.99
print(f"Variable 'price': {price}")

#  string data type
name = "Alice"
print(f"Variable 'name': {name}")

#  boolean data type
is_active = True
print(f"Variable 'is_active': {is_active}")

#  list data type
fruits = ["apple", "banana", "cherry"]
print(f"Variable 'fruits': {fruits}")

#  tuple data type
coordinates = (10, 20)
print(f"Variable 'coordinates': {coordinates}")

#  dictionary data type
person = {"name": "Bob", "age": 25}
print(f"Variable 'person': {person}")



